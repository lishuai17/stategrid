	public void countType(){
		String typeStr=JSON.toJSON(this).toString();
		String[] typeArr=typeStr.split(",");
		for(String colStr:typeArr){
			if(colStr.indexOf("id")<0){
				if(colStr.indexOf("峰")>=0){
					this.count2a++;
				}else if(colStr.indexOf("谷")>=0){
					this.count3a++;
				}
			}
		}
	}
	
